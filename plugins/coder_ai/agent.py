def _select_template(goal: str) -> str:
    """Choose a source template based on keywords in the goal."""
    goal_lower = goal.lower()

    if any(kw in goal_lower for kw in ["calculator", "calc", "arithmetic", "math"]):
        return _CALCULATOR_SOURCE

    # Default: generic local API
    return _API_SOURCE


