import json
import uuid
from datetime import datetime
from pathlib import Path

DATA_FILE = Path("tasks.json")


def load_tasks() -> list:
    """Load all tasks from persistent storage."""
    if not DATA_FILE.exists():
        return []
    try:
        with open(DATA_FILE) as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return []


def save_tasks(tasks: list) -> None:
    """Persist tasks to storage using atomic write."""
    tmp = DATA_FILE.with_suffix(".tmp")
    with open(tmp, "w") as f:
        json.dump(tasks, f, indent=2)
    tmp.replace(DATA_FILE)


def create_task(title: str, priority: str = "MEDIUM", due_date: str = "") -> dict:
    """Create and persist a new task. Returns the created task."""
    if not title.strip():
        raise ValueError("Task title cannot be empty.")
    priority = priority.upper()
    if priority not in ("LOW", "MEDIUM", "HIGH", "CRITICAL"):
        priority = "MEDIUM"
    task = {
        "id":         str(uuid.uuid4())[:8],
        "title":      title.strip(),
        "status":     "PENDING",
        "priority":   priority,
        "due_date":   due_date,
        "created_at": datetime.utcnow().isoformat(),
        "updated_at": datetime.utcnow().isoformat(),
    }
    tasks = load_tasks()
    tasks.append(task)
    save_tasks(tasks)
    return task


def list_tasks(status: str = "") -> list:
    """Return all tasks, optionally filtered by status."""
    tasks = load_tasks()
    if status:
        tasks = [t for t in tasks if t["status"] == status.upper()]
    return sorted(tasks, key=lambda t: ("LOW","MEDIUM","HIGH","CRITICAL").index(t.get("priority","MEDIUM")), reverse=True)


def complete_task(task_id: str) -> bool:
    """Mark a task as DONE. Returns True if found."""
    tasks = load_tasks()
    for task in tasks:
        if task["id"] == task_id:
            task["status"]     = "DONE"
            task["updated_at"] = datetime.utcnow().isoformat()
            save_tasks(tasks)
            return True
    return False


def delete_task(task_id: str) -> bool:
    """Delete a task by ID. Returns True if found."""
    tasks = load_tasks()
    new   = [t for t in tasks if t["id"] != task_id]
    if len(new) == len(tasks):
        return False
    save_tasks(new)
    return True


def get_summary() -> dict:
    """Return task count statistics."""
    tasks = load_tasks()
    return {
        "total":    len(tasks),
        "pending":  sum(1 for t in tasks if t["status"] == "PENDING"),
        "done":     sum(1 for t in tasks if t["status"] == "DONE"),
        "critical": sum(1 for t in tasks if t["priority"] == "CRITICAL"),
    }


