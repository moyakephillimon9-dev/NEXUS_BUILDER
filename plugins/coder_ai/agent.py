def list_directory(path: str = ".") -> list:
    """List directory contents with metadata."""
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"Path not found: {path}")
    entries = []
    for item in sorted(p.iterdir()):
        stat = item.stat()
        entries.append({
            "name":       item.name,
            "type":       "dir" if item.is_dir() else "file",
            "size":       stat.st_size,
            "modified":   datetime.fromtimestamp(stat.st_mtime).isoformat(),
            "extension":  item.suffix.lower() if item.is_file() else "",
        })
    return entries


def search_files(root: str, pattern: str) -> list:
    """Recursively search for files matching a glob pattern."""
    return [str(p) for p in Path(root).rglob(pattern)]


def copy_file(src: str, dst: str) -> str:
    """Copy a file and return the destination path."""
    src_path = Path(src)
    if not src_path.is_file():
        raise FileNotFoundError(f"Source not found: {src}")
    dst_path = Path(dst)
    dst_path.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src_path, dst_path)
    return str(dst_path)


def move_file(src: str, dst: str) -> str:
    """Move a file and return the destination path."""
    Path(dst).parent.mkdir(parents=True, exist_ok=True)
    shutil.move(src, dst)
    return dst


def delete_file(path: str, confirm: bool = False) -> bool:
    """Delete a file. Requires confirm=True."""
    if not confirm:
        raise PermissionError("Set confirm=True to delete files.")
    p = Path(path)
    if p.is_file():
        p.unlink()
        return True
    if p.is_dir():
        shutil.rmtree(p)
        return True
    return False


def checksum(filepath: str, algorithm: str = "sha256") -> str:
    """Compute a file checksum."""
    h = hashlib.new(algorithm)
    with open(filepath, "rb") as f:
        for chunk in iter(lambda: f.read(65536), b""):
            h.update(chunk)
    return h.hexdigest()


def create_directory(path: str) -> str:
    """Create a directory and all parents."""
    Path(path).mkdir(parents=True, exist_ok=True)
    return path


def get_disk_usage(path: str = ".") -> dict:
    """Return disk usage statistics for a path."""
