def add(a, b):
    """Return the sum of a and b."""
    return a + b


def subtract(a, b):
    """Return the difference of a and b."""
    return a - b


def multiply(a, b):
    """Return the product of a and b."""
    return a * b


def divide(a, b):
    """Return the quotient of a and b. Raises ValueError on division by zero."""
    if b == 0:
        raise ValueError("Cannot divide by zero.")
    return a / b


def calculate(expression: str):
    """
    Parse and evaluate a simple arithmetic expression string.
    Supported operators: + - * /
    Example: calculate("10 + 5") -> 15.0
    """
    parts = expression.strip().split()

    if len(parts) != 3:
        raise ValueError(
            f"Invalid expression '{expression}'. "
            "Expected format: <number> <operator> <number>"
        )

    left, operator, right = parts

    left  = float(left)
    right = float(right)

    if operator == "+":
        return add(left, right)
    elif operator == "-":
        return subtract(left, right)
    elif operator == "*":
        return multiply(left, right)
    elif operator == "/":
        return divide(left, right)
    else:
        raise ValueError(f"Unknown operator: {operator}")


def run_repl():
    """Interactive calculator loop."""
    print("=" * 40)
    print("NEXUS Calculator")
    print("Format : <number> <operator> <number>")
    print("Example: 10 + 5")
    print("Type 'exit' to quit.")
    print("=" * 40)

    while True:
        try:
            expression = input("calc> ").strip()

            if expression.lower() == "exit":
                print("Goodbye.")
                break

            result = calculate(expression)
            print(f"= {result}")

        except (ValueError, ZeroDivisionError) as error:
            print(f"[Error] {error}")


if __name__ == "__main__":
    run_repl()
