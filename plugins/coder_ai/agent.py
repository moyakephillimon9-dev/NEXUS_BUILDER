class CoderAI:
    """
    The software engineering worker of NEXUS.

    Responsibilities:
    - Generate executable source code
    - Commit code artifacts into Shared Memory
    - Prepare projects for automated review
    """

    def __init__(self, shared_memory):
        self.memory = shared_memory
        print("[Coder AI] Connected to Shared Memory.")

    def start(self):
        print("[Coder AI] Ready.")

    def process_project_task(self, task_id: str):

        project_key = f"active_project_{task_id}"
        project = self.memory.read(project_key)

        if project is None:
            print(f"[Coder AI] Error: Project '{task_id}' not found.")
            return

        print(f"[Coder AI] Processing Task: {task_id}")
        print(f"[Coder AI] Goal: {project['goal']}")

        # ----------------------------------------------------------
        # Select and generate source code based on the project goal
        # ----------------------------------------------------------

        generated_source = _select_template(project["goal"])

        # ----------------------------------------------------------
        # Store code artifact
        # ----------------------------------------------------------

        project["code"] = {
            "language": "Python",
            "version": "0.1.0",
            "author": "Coder AI",
            "generated_at": datetime.utcnow().isoformat(),
            "status": "INITIAL_IMPLEMENTATION_COMPLETE",
            "files": [
                "main.py"
            ],
            "source": generated_source
        }

        project["status"] = "PROGRAMMING_PHASE_COMPLETE"

        self.memory.write(project_key, project)

        print("[Coder AI] Executable source generated.")
        print("[Coder AI] Shared Memory Updated.")
