def _health_check_script(project: dict) -> str:
    goal = project.get("goal", "NEXUS Application")
    arch = project.get("architecture", {})
    return f'''\
"""
Health Check — Auto-generated by NEXUS Monitoring AI
Application: {goal}
"""

import sys
import time
import json
import socket
import sqlite3
from pathlib import Path
from datetime import datetime, timezone

APP_NAME    = "{goal}"
DB_PATH     = Path("data/database.sqlite")
CHECKS      = []
START_TIME  = time.time()


def register(name):
    """Decorator to register a health check."""
    def decorator(fn):
        CHECKS.append((name, fn))
        return fn
    return decorator


@register("application")
def check_application() -> dict:
    """Verify the application module is importable."""
    try:
        import main  # noqa: F401
        return {{"status": "pass", "message": "main.py loaded successfully"}}
    except Exception as err:
        return {{"status": "fail", "message": str(err)}}


@register("database")
def check_database() -> dict:
    """Verify the SQLite database is accessible."""
    if not DB_PATH.exists():
        return {{"status": "warn", "message": "Database file not found — will be created on first run"}}
    try:
        conn = sqlite3.connect(str(DB_PATH))
        conn.execute("SELECT 1")
        conn.close()
        return {{"status": "pass", "message": f"Database responsive at {{DB_PATH}}"}}
    except Exception as err:
        return {{"status": "fail", "message": str(err)}}


@register("filesystem")
def check_filesystem() -> dict:
    """Verify write access to the working directory."""
    probe = Path(".nexus_probe")
    try:
        probe.write_text("ok")
        probe.unlink()
        return {{"status": "pass", "message": "Filesystem writable"}}
    except Exception as err:
        return {{"status": "fail", "message": str(err)}}


@register("memory")
def check_memory() -> dict:
    """Report available memory."""
    try:
        import resource
        usage = resource.getrusage(resource.RUSAGE_SELF).ru_maxrss
        return {{"status": "pass", "message": f"RSS: {{usage}} KB"}}
    except Exception:
        return {{"status": "pass", "message": "Memory check not supported on this platform"}}


def run_checks() -> dict:
    results = {{}}
    overall = "pass"

    for name, fn in CHECKS:
        try:
            result = fn()
        except Exception as err:
            result = {{"status": "fail", "message": str(err)}}

        results[name] = result

        if result["status"] == "fail":
            overall = "fail"
        elif result["status"] == "warn" and overall == "pass":
            overall = "warn"

    return {{
        "status":    overall,
        "app":       APP_NAME,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "uptime_s":  round(time.time() - START_TIME, 3),
        "checks":    results,
    }}


if __name__ == "__main__":
    report = run_checks()
    print(json.dumps(report, indent=2))
    sys.exit(0 if report["status"] in ("pass", "warn") else 1)
'''


