def _metrics_script(project: dict) -> str:
    goal = project.get("goal", "NEXUS Application")
    return f'''\
"""
Metrics Collector — Auto-generated by NEXUS Monitoring AI
Application: {goal}
"""

import time
import json
import threading
from collections import defaultdict
from datetime import datetime, timezone
from pathlib import Path

METRICS_FILE = Path("logs/metrics.json")
METRICS_FILE.parent.mkdir(parents=True, exist_ok=True)

_counters:   dict = defaultdict(int)
_gauges:     dict = {{}}
_histograms: dict = defaultdict(list)
_lock = threading.Lock()


def increment(name: str, value: int = 1, tags: dict | None = None):
    """Increment a counter metric."""
    key = _key(name, tags)
    with _lock:
        _counters[key] += value


def gauge(name: str, value: float, tags: dict | None = None):
    """Set a gauge metric to an absolute value."""
    key = _key(name, tags)
    with _lock:
        _gauges[key] = value


def observe(name: str, value: float, tags: dict | None = None):
    """Record a histogram observation."""
    key = _key(name, tags)
    with _lock:
        _histograms[key].append(value)


def timer(name: str, tags: dict | None = None):
    """Context manager to time a block of code."""
    class _Timer:
        def __enter__(self_):
            self_._start = time.perf_counter()
            return self_
        def __exit__(self_, *_):
            duration_ms = (time.perf_counter() - self_._start) * 1000
            observe(name, duration_ms, tags)
    return _Timer()


def snapshot() -> dict:
    """Return a point-in-time snapshot of all metrics."""
    with _lock:
        hist_summary = {{}}
        for k, vals in _histograms.items():
            if vals:
                hist_summary[k] = {{
                    "count": len(vals),
                    "min":   round(min(vals), 4),
                    "max":   round(max(vals), 4),
                    "avg":   round(sum(vals) / len(vals), 4),
                }}
        return {{
            "app":        "{goal}",
            "timestamp":  datetime.now(timezone.utc).isoformat(),
            "counters":   dict(_counters),
            "gauges":     dict(_gauges),
            "histograms": hist_summary,
        }}


def flush():
    """Write current metrics snapshot to disk."""
    data = snapshot()
    with open(METRICS_FILE, "w") as f:
        json.dump(data, f, indent=2)
    return data


def _key(name: str, tags: dict | None) -> str:
    if not tags:
        return name
    tag_str = ",".join(f"{{k}}={{v}}" for k, v in sorted(tags.items()))
    return f"{{name}}{{{{{tag_str}}}}}"


if __name__ == "__main__":
    # Demo: record some metrics
    increment("requests_total", tags={{"method": "GET"}})
    increment("requests_total", tags={{"method": "POST"}})
    gauge("active_connections", 42)
    with timer("response_time_ms"):
        time.sleep(0.01)
    data = flush()
    print(json.dumps(data, indent=2))
'''


