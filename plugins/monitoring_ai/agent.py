class MonitoringAI:
    """
    NEXUS Observability Engineer.

    Generates production-ready monitoring artefacts:
    health check, logging configuration, metrics collector,
    and alerting rules — written to the deployment folder.
    """

    def __init__(self, shared_memory, deployment_root: str = "deployments"):
        self.memory          = shared_memory
        self.deployment_root = deployment_root
        print("[Monitoring AI] Connected to Shared Memory.")

    def start(self):
        print("[Monitoring AI] Observability Engineering Engine Ready.")

    def process_project_task(self, task_id: str):

        key     = f"active_project_{task_id}"
        project = self.memory.read(key)

        if project is None:
            print("[Monitoring AI] Project not found.")
            return

        print(f"[Monitoring AI] Generating observability artefacts: {task_id}")

        goal_slug = project.get("goal", "project").lower().replace(" ", "_")[:40]
        folder    = os.path.join(self.deployment_root, f"{task_id}_{goal_slug}")
        mon_dir   = os.path.join(folder, "monitoring")
        os.makedirs(mon_dir, exist_ok=True)

        artefacts = {
            os.path.join(mon_dir, "health_check.py"):  _health_check_script(project),
            os.path.join(mon_dir, "logger_config.py"): _logger_config(project),
            os.path.join(mon_dir, "metrics.py"):        _metrics_script(project),
            os.path.join(mon_dir, "alerts.json"):       _alert_rules(project),
        }

        written = []
        for path, content in artefacts.items():
            with open(path, "w") as f:
                f.write(content)
            written.append(os.path.basename(path))

        mon_report = {
            "generated_at": datetime.utcnow().isoformat(),
            "artefacts":    written,
            "monitoring_dir": mon_dir,
        }

        project["monitoring"] = mon_report
        project["status"]     = "MONITORING_COMPLETE"
        self.memory.write(key, project)

        for a in written:
            print(f"[Monitoring AI]   ✓ {a}")
